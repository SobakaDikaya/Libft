#include "ft_libft.h"

char		*ft_strchr(const char *str, int ch)
{
	char	*s;
	int		i;

	i = 0;
	s = (char *)str;
	while (s[i] != ch)
	{
		if (s[i] == '\0')
		{
			return (NULL);
		}
		i++;
	}
	return (&s[i]);
}
